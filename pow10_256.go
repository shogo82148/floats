// NewFloat256Pow10 returns 10**n, the base-10 exponential of n.
//
// Special cases are:
//
//	NewFloat256Pow10(n) =    0 for n < -78984
//	NewFloat256Pow10(n) = +Inf for n > 78913
func NewFloat256Pow10(n int) Float256 {
	if 0 <= n && n <= 78913 {
		return newFloat256Pow10Table256[uint(n)/256].Mul(newFloat256Pow10Table[uint(n)%256])
	}
	if -78984 <= n && n < 0 {
		return newFloat256Pow10Table256Neg[uint(-n)/256].Quo(newFloat256Pow10Table[uint(-n)%256])
	}
	if n > 0 {
		return NewFloat256Inf(1)
	}
	return Float256{} // 0
}
