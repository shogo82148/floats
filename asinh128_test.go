package floats

import (
	"math"
	"testing"
)

func TestFloat128_Asinh(t *testing.T) {
	tests := []struct {
		x    Float128
		want string
	}{
		{exact128(0), "0"},
		{exact128(0x1p-59), "1.734723475976807094411924481391906737411209404266863396438627452099126993751360531E-18"},
		{exact128(0.25), "0.247466461547263452944781549788359289253766903098567696469117357944365179443666365"},
		{exact128(0.5), "0.4812118250596034474977589134243684231351843343856605196610181688401638676082217744"},
		{exact128(1), "0.8813735870195430252326093249797923090281603282616354107532956086533771842220260878"},
		{exact128(21), "3.738236030261542702926345171286353186644971465768736202615459010981959402821114935"},
		{exact128(0x1p59), "41.58883083359671856503392728749059408528232444614157925234079195342585455113665898"},

		{exact128(-0), "-0"},
		{exact128(-0.25), "-0.247466461547263452944781549788359289253766903098567696469117357944365179443666365"},
		{exact128(-0.5), "-0.4812118250596034474977589134243684231351843343856605196610181688401638676082217744"},
		{exact128(-1), "-0.8813735870195430252326093249797923090281603282616354107532956086533771842220260878"},
		{exact128(-21), "-3.738236030261542702926345171286353186644971465768736202615459010981959402821114935"},
	}

	for _, tt := range tests {
		got := tt.x.Asinh()
		if !close128(got, tt.want) {
			t.Errorf("Asinh(%v) = %v; want %v", tt.x, got, tt.want)
		}
	}

	strictTests := []struct {
		x    Float128
		want Float128
	}{
		{exact128(0), exact128(0)},
		{exact128(math.Copysign(0, -1)), exact128(math.Copysign(0, -1))},

		// special cases
		{exact128(math.Inf(1)), exact128(math.Inf(1))},
		{exact128(math.Inf(-1)), exact128(math.Inf(-1))},
		{exact128(math.NaN()), exact128(math.NaN())},
	}

	for _, tt := range strictTests {
		got := tt.x.Asinh()
		if !eq128(got, tt.want) {
			t.Errorf("Asinh(%v) = %v; want %v", tt.x, got, tt.want)
		}
	}
}
