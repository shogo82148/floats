// NewFloat128Pow10 returns 10**n, the base-10 exponential of n.
//
// Special cases are:
//
//	NewFloat128Pow10(n) =    0 for n < -4965
//	NewFloat128Pow10(n) = +Inf for n > 4932
func NewFloat128Pow10(n int) Float128 {
	if 0 <= n && n <= 4932 {
		return newFloat128Pow10Table64[uint(n)/64].Mul(newFloat128Pow10Table[uint(n)%64])
	}
	if -4965 <= n && n < 0 {
		return newFloat128Pow10Table64Neg[uint(-n)/64].Quo(newFloat128Pow10Table[uint(-n)%64])
	}
	if n > 0 {
		return NewFloat128Inf(1)
	}
	return Float128{} // 0
}
